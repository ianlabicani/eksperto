    <div class="modal-dialog">
        <div class="modal-content">
            <div class="modal-header">
                <h5 class="modal-title" id="deleteJobModalLabel">Confirm Deletion</h5>
                <button type="button" class="btn-close" data-bs-dismiss="modal" aria-label="Close"></button>
            </div>
            <div class="modal-body">
                Are you sure you want to delete this job listing? This action cannot be undone.
            </div>
            <div class="modal-footer">
                <button type="button" class="btn btn-secondary" data-bs-dismiss="modal">Cancel</button>
                <form id="deleteJobForm" method="POST">
                    @csrf
                    @method('DELETE')
                    <button type="submit" class="btn btn-danger">Delete</button>
                </form>
            </div>
        </div>
    </div>
</div>

<script>
    document.addEventListener("DOMContentLoaded", function () {
        var deleteModal = document.getElementById("deleteJobModal");
        deleteModal.addEventListener("show.bs.modal", function (event) {
            var button = event.relatedTarget; // Button that triggered the modal
            var jobId = button.getAttribute("data-job-id"); // Get job ID
            var form = document.getElementById("deleteJobForm");
            form.action = `/client/job-listings/${jobId}`; // Update form action dynamically
        });
    });
</script>